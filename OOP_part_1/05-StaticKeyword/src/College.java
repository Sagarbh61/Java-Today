// A program demonstrating static final constants

class College {

    static final String COLLEGE_NAME = "Ratna RajyaLaxmi Campus";

    public static void main(String[] args)
    {
        System.out.println("College Name: "+COLLEGE_NAME);
    }
}
/*
Note:
1) static final variables are constants.
2) Their values cannot be changed.
3) Conventionally written in uppercase letters.
*/
